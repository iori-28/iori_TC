"""
Buatkan 3 contoh operator perbandingan
"""
# Contoh "equal to" or "sama dengan"
a = 5
b = 5

result = (a == b)  # True, karena 5 itu sama dengan 5
print(result)  # Output: True

# Contoh "greater than" or "lebih besar dari"
a = 10
b = 5

result = (a > b)  # True, karena 10 itu lebih besar dari 5
print(result)  # Output: True

# Contoh "less than or equal to" or "lebih kecil dari sama dengan"
a = 3
b = 5

result = (a <= b)  # True, karena 3 itu lebih kecil dari sama dengan 5
print(result)  # Output: True

